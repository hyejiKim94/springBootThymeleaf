DROP TABLE IF EXISTS COMMON_MASTER CASCADE;
CREATE TABLE COMMON_MASTER (
    MASTER_CD VARCHAR(20),
    MASTER_VALUE VARCHAR(255),
    MASTER_CD_NAME VARCHAR(255) NOT NULL,
    MASTER_VALUE_NAME VARCHAR(255) NOT NULL,
    DISPLAY_ORDER INT,
    CREATED_DATE TIMESTAMP,
    UPDATED_DATE TIMESTAMP,
    PRIMARY KEY(MASTER_CD, MASTER_VALUE)
);

DROP TABLE IF EXISTS EMPLOYEE CASCADE;
CREATE TABLE EMPLOYEE (
    EMPLOYEE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    BIRTH CHARACTER(8) NOT NULL,
    EMPLOYEE_NAME VARCHAR(50) NOT NULL,
    DEPARTMENT_CD CHARACTER(2),
    POSITION_CD CHARACTER(2),
    PROJECT_NO VARCHAR(20),
    DELETE_FLAG CHAR(1) NOT NULL DEFAULT '0',
    CREATED_DATE TIMESTAMP,
    UPDATED_DATE TIMESTAMP,
    PRIMARY KEY(EMPLOYEE_ID)
);

DROP TABLE IF EXISTS OVERTIME_APPLY CASCADE;
CREATE TABLE OVERTIME_APPLY(
    APPLY_NO BIGINT GENERATED BY DEFAULT AS IDENTITY,
    APPLY_EMPLOYEE_ID BIGINT NOT NULL,
    APPROVAL_EMPLOYEE_ID BIGINT,
    YEAR_AND_MONTH CHARACTER(6) NOT NULL,
    APPLY_HOUR INT NOT NULL,
    STATUS CHARACTER(2),
    REASON VARCHAR(255),
    COMMENT VARCHAR(255),
    CREATED_DATE TIMESTAMP,
    UPDATED_DATE TIMESTAMP,
    PRIMARY KEY(APPLY_NO)
);

DROP TABLE IF EXISTS PROJECT CASCADE;
CREATE TABLE PROJECT(
    PROJECT_NO BIGINT GENERATED BY DEFAULT AS IDENTITY,
    PROJECT_NAME VARCHAR(255) NOT NULL,
    WORK_TYPE_CD CHARACTER(2),
    WORK_PLACE VARCHAR(255),
    MANAGER_EMPLOYEE_NO BIGINT,
    START_DATE TIMESTAMP,
    END_DATE TIMESTAMP,
    DELETE_FLAG CHARACTER(1) NOT NULL DEFAULT '0',
    PRIMARY KEY(PROJECT_NO)
);